#include<stdio.h>
#include<stdlib.h>
int main(int argc, char* argv[])
{
	char ch;
	if(argc!=2){
		printf("Arguments error!\n");
		exit(-1);
	}
	if(freopen(argv[1],"r",stdin) == NULL) {       /* fp 指向 filename */
		printf("Can't open %s file!\n",argv[1]);
		exit(-1);
	}

	while(scanf("%c", &ch)!=EOF)          /* 从filename中读字符 */ //括号问题 
	   putchar(ch);                  /* 向显示器中写字符 */
	fclose(stdout);                      /* 关闭filename */
 	return 0;	
}

